/**
 * Tipos de  datos
 * -Uso de operador typeof
 * -Muestra datos desde la consola del navegador
 */

//Number - Para todos los tipos de numeros. Enteros y decimales
console.log(typeof 50)

//String - Cadena de texto
console.log(typeof 'Alan')

//Boolean - False o True(palabras reservadas)
console.log(typeof true)

//Null - Tiene un valor de 'no valor'
console.log(typeof null)

//Symbol -- Nuevo en ECMAScript 2015

//Undefined - Variable a la que no se le ha asignado un valor
console.log(typeof undefined)


